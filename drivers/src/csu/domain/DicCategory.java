package csu.domain;

public class DicCategory {
	private static final long serialVersionUID = -3856518279921410532L;
	private Integer code, years_old;
	private String cname;
	public Integer getCode() {
		return code;
	}
	public void setCode(Integer code) {
		this.code = code;
	}
	public Integer getYears_old() {
		return years_old;
	}
	public void setYears_old(Integer years_old) {
		this.years_old = years_old;
	}
	public String getCname() {
		return cname;
	}
	public void setCname(String cname) {
		this.cname = cname;
	}
	
	

}
